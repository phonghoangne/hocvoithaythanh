package org.OOP.ultils;

public class Helper {


    public static void print(int message) {
        System.out.println(message);
    }

    public static void print(float message) {
        System.out.println(message);
    }

    public static void print(double message) {
        System.out.println(message);
    }

    public static void print(char message) {
        System.out.println(message);
    }

    public static void print(boolean message) {
        System.out.println(message);
    }

    public static void print(long message) {
        System.out.println(message);
    }

    public static void print(short message) {
        System.out.println(message);
    }

    public static void print(byte message) {
        System.out.println(message);
    }

    public static void print(Object message) {
        System.out.println(message);
    }

    public static void print() {
        System.out.println();
    }

    public static void print(String message, int value) {
        System.out.println(message + value);
    }

    public static void print(String message, float value) {
        System.out.println(message + value);
    }

    public static void print(String message, double value) {
        System.out.println(message + value);
    }

    public static void print(String message, char value) {
        System.out.println(message + value);
    }

    public static void print(String message, boolean value) {
        System.out.println(message + value);
    }

    public static void print(String message, long value) {
        System.out.println(message + value);
    }

    public static void print(String message, short value) {
        System.out.println(message + value);
    }

    public static void print(String message, byte value) {
        System.out.println(message + value);
    }

    public static void print(String message, Object value) {
        System.out.println(message + value);
    }

    public static void print(String message, String value) {
        System.out.println(message + value);
    }

    public static void print(String message, String value, String value2) {
        System.out.println(message + value + value2);
    }

    public static void print(String message, String value, String value2, String value3) {
        System.out.println(message + value + value2 + value3);
    }




}
